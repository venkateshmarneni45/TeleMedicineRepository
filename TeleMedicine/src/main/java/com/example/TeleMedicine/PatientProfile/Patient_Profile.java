package com.example.TeleMedicine.PatientProfile;

public class Patient_Profile {
	private int id;
	private String name;
	private String mobile;
	private int age;
	private String gender;
	private String height;
	private String weight;
	private String state;
	private String city;
	private String bloodGroup;
	private String img;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getBloodGroup() {
		return bloodGroup;
	}
	public void setBloodGroup(String bloodGroup) {
		this.bloodGroup = bloodGroup;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	@Override
	public String toString() {
		return "Patient_Profile [id=" + id + ", name=" + name + ", mobile=" + mobile + ", age=" + age + ", gender="
				+ gender + ", height=" + height + ", weight=" + weight + ", state=" + state + ", city=" + city
				+ ", bloodGroup=" + bloodGroup + ", img=" + img + "]";
	}
}
